# Cretaing a list using square brackets '[]'
a = [1, 3, 4, 23, 343, 66, 69]

# Print the list using print() function
print(a)

# Accessing the list's individual or some desired elements of lists using indices as a[0] (first element), a[1] (second element) and so on..
print(a[3])

# Changing the values in a lists at the specific position
a[4] = 59
print(a)

# We can create a list with different datatypes as elements
c = [3, "tt", 6.9, True]
print(c)
