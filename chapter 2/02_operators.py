a = 69
b = 420

# Arithmetic Operators

print("The value of 69 + 420 is:", a + b)  # addition (+) operation
print("The value of 69 - 420 is:", a - b)  # subtration (-) operation
print("The value of 69 * 420 is:", a * b)  # multiplication (*) operation
print("The value of 69 / 420 is:", a / b)  # division (/) operation

# Assignment operators
c = 34
c += 2  # Increments by 2
print(c)

# comparison operators
d = (b > a)
print(d)

# logical operators
bool1 = True
bool2 = False
print("The value of bool1 and bool2 is", (bool1 and bool2))
print("The value of bool1 or bool2 is", (bool1 or bool2))
print("The value of not bool2 is", (not bool2))
